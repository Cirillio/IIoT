namespace ModbusClient.Models;

/// <summary>
/// Перечисление типов данных датчиков.
/// Соответствует пользовательскому типу данных (ENUM) 'sensor_data_type' в базе данных PostgreSQL.
/// Определяет способ обработки "сырых" данных.
/// </summary>
public enum SensorDataType
{
    /// <summary>
    /// Аналоговый сигнал (например, температура, давление).
    /// Требует линейной интерполяции (масштабирования) от InputMin/Max к OutputMin/Max.
    /// </summary>
    ANALOG,

    /// <summary>
    /// Дискретный сигнал (0 или 1).
    /// Используется для состояний (вкл/выкл, открыто/закрыто).
    /// </summary>
    DIGITAL,

    /// <summary>
    /// Виртуальный датчик.
    /// Значение вычисляется на основе других датчиков или сложных формул, а не считывается напрямую с порта.
    /// </summary>
    VIRTUAL,
}
